"""
1. GAN (Generative Adversarial Networks)
- 적용 상황: 이미지 생성, 스타일 변환, 객체 합성 등 다양한 분야에서 사용됨.
- 장점: 높은 품질의 이미지를 생성할 수 있으며, 비지도 학습으로 다양한 데이터셋에 적용 가능.
- 단점: 모델 학습이 어렵고, 모드 붕괴(mode collapse) 문제가 발생할 수 있음.

2. VAE (Variational Autoencoders)
- 적용 상황: 이미지 생성, 이미지 압축, 이미지 수정 등에 사용됨.
- 장점: 생성된 이미지가 원래 데이터와 비슷한 통계적 특성을 가짐. 이론적 배경이 잘 정립되어 있어 이해하기 쉬움.
- 단점: GAN에 비해 생성하는 이미지의 품질이 낮을 수 있음.

3. StyleGAN
- 적용 상황: 고해상도 이미지 생성, 얼굴 생성, 스타일 변환 등에서 사용됨.
- 장점: 매우 높은 품질의 이미지를 생성할 수 있으며, 다양한 스타일 변환을 지원.
- 단점: 모델의 규모가 크고 학습에 많은 시간과 리소스가 필요.

4. Pix2Pix
- 적용 상황: 이미지-이미지 변환 작업에 사용됨. 예를 들어, 흑백 이미지를 컬러로 변환하는 등의 작업에 사용됨.
- 장점: 짝지어진 데이터를 활용해 특정 작업에 최적화된 결과를 얻을 수 있음.
- 단점: 학습을 위한 짝지어진 데이터셋이 필요하며, 다양한 변환에는 적합하지 않을 수 있음.

5. CycleGAN
- 적용 상황: 짝지어지지 않은 데이터셋을 사용해 이미지 스타일 변환을 하는 작업에 사용됨.
- 장점: 짝지어지지 않은 데이터셋을 사용할 수 있으며, 양방향 변환을 지원.
- 단점: 학습이 불안정할 수 있으며, 이미지 품질이 다소 떨어질 수 있음.
"""
